// ─── Parser ──────────────────────────────────────────────────────────────────

/**
 * Extract top-level methods from Object.assign(target, { ... }) block.
 * Returns array of { name, body } where body includes the full method text
 * (declaration + body + trailing comma).
 */
function extractMethods(content) {
    const lines = content.split('\n');
    const methods = [];

    // Find the Object.assign line
    let startIdx = -1;
    for (let i = 0; i < lines.length; i++) {
        if (lines[i].match(/^Object\.assign\(\w+,\s*\{/)) {
            startIdx = i + 1;
            break;
        }
    }
    if (startIdx === -1) return methods;

    // Method declaration pattern at 4 spaces indentation
    const methodDeclPattern = /^    (async\s+)?(\w+)\s*[\(:]|^    (async\s+)?(\w+)\s*\(/;

    let currentName = null;
    let currentLines = [];

    for (let i = startIdx; i < lines.length; i++) {
        const line = lines[i];

        // End of Object.assign block
        if (line.match(/^\}\);/)) {
            if (currentName) {
                methods.push({ name: currentName, body: currentLines.join('\n') });
            }
            break;
        }

        // Check if this line starts a new method
        const match = line.match(methodDeclPattern);
        if (match) {
            const name = match[2] || match[4];
            // Save previous method
            if (currentName) {
                methods.push({ name: currentName, body: currentLines.join('\n') });
            }
            currentName = name;
            currentLines = [line];
        } else if (currentName) {
            currentLines.push(line);
        }
    }

    return methods;
}

// ─── Main ────────────────────────────────────────────────────────────────────

const pagesContent = readFileSync(join(PAGES_DIR, 'pages-deferred.js'), 'utf-8');
const handlersContent = readFileSync(join(HANDLERS_DIR, 'handlers-deferred.js'), 'utf-8');

const pageMethods = extractMethods(pagesContent);
const handlerMethods = extractMethods(handlersContent);

console.log(`Extracted ${pageMethods.length} pages and ${handlerMethods.length} handlers`);

// Group methods by chunk
const CHUNKS = ['inventory-catalog', 'sales-orders', 'tools-tasks', 'intelligence', 'settings-account', 'community-help'];

const pageChunks = {};
const handlerChunks = {};
CHUNKS.forEach(c => { pageChunks[c] = []; handlerChunks[c] = []; });

// Sort pages into chunks
for (const m of pageMethods) {
    const chunk = PAGES_CHUNK_MAP[m.name];
    if (!chunk) {
        console.warn(`[WARN] Unmapped page: ${m.name} — skipping (will remain in deferred)`);
        continue;
    }
    pageChunks[chunk].push(m);
}

// Sort handlers into chunks (skip core handlers)
let coreCount = 0;
for (const m of handlerMethods) {
    if (CORE_HANDLERS.has(m.name)) {
        coreCount++;
        continue; // These move to handlers-core.js
    }
    const chunk = HANDLERS_CHUNK_MAP[m.name];
    if (!chunk) {
        console.warn(`[WARN] Unmapped handler: ${m.name} — defaulting to settings-account`);
        handlerChunks['settings-account'].push(m);
        continue;
    }
    handlerChunks[chunk].push(m);
}

console.log(`${coreCount} handlers assigned to core (cross-cutting)`);

// Write chunk files
for (const chunk of CHUNKS) {
    const pagesFile = join(PAGES_DIR, `pages-${chunk}.js`);
    const handlersFile = join(HANDLERS_DIR, `handlers-${chunk}.js`);

    // Write pages chunk
    const pagesBody = pageChunks[chunk].map(m => m.body).join('\n\n');
    const pagesOut = `'use strict';
// Route-group chunk: ${chunk} (pages)
// Auto-generated by scripts/split-deferred-chunks.js

Object.assign(pages, {
${pagesBody}
});
`;
    writeFileSync(pagesFile, pagesOut);
    console.log(`  pages-${chunk}.js — ${pageChunks[chunk].length} pages`);

    // Write handlers chunk
    const handlersBody = handlerChunks[chunk].map(m => m.body).join('\n\n');
    const handlersOut = `'use strict';
// Route-group chunk: ${chunk} (handlers)
// Auto-generated by scripts/split-deferred-chunks.js

Object.assign(handlers, {
${handlersBody}
});
`;
    writeFileSync(handlersFile, handlersOut);
    console.log(`  handlers-${chunk}.js — ${handlerChunks[chunk].length} handlers`);
}

console.log('\nDone! Generated 12 chunk files.');
